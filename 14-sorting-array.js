// Sroting an Array
const cars = ["Lamborghini", "Ferrari", "Bugatti", "Supra"];
cars.sort();


// Reversing an Array
const brands = ["LV", "Rolex", "Dior", "Tom Ford"];
brands.sort();
brands.reverse();

// Sorting Numbers
const numbers = [100, 50, 200, 150];
numbers.sort((a, b) => a - b);


console.log(cars);
console.log(brands);
console.log(numbers);