// toString()
let a = 14;
a.toString();

// toExponential()
let b = 14.234;
b.toExponential(2);

// toFixed()
let c = 14.234;
c.toFixed(2);

// toPrecision()
let d = 14.234;
d.toPrecision(2);

// valueOf()
let e = 14.234;
e.valueOf();

console.log(a.toString());
console.log(b.toExponential(2));
console.log(c.toFixed(2));
console.log(d.toPrecision(2));
console.log(e.valueOf());

